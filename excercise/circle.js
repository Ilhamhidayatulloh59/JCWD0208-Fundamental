// Write a code to find diameter, circumference and area of a circle.
let jari = 7
const pi = 3.14

// rumus menemukan dimater -> 2 * jari-jari
let diameter = 2 * jari

// rumus menemukan keliling lingkaran -> 2 * jari * pi
let keliling = 2 * jari * pi

// rumus menemukan luar lingkaran -> pi * jari * jari
let luas = pi * jari ** 2

console.log(`diameter, keliling dan luas lingkaran dengan jari-jari ${jari}
adalah ${diameter}, ${keliling} dan ${luas}`)